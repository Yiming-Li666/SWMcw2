package model;

public class SettingModel {
	private int bgCounter = 801;
	private int obCounter = 801;
	private int dif = 301;
	private int ghostNum = 301;
	
	public void setBgCounter(int value) {
		bgCounter = value;
	}
	
	public int getBgCounter() {
		return bgCounter;
	}
	
	public void setObCounter(int value) {
		obCounter = value;
	}
	
	public int getObCounter() {
		return obCounter;
	}
	
	public void setDif(int value) {
		dif = value;
	}
	
	public int getDif() {
		return dif;
	}
	
	public void setGhostNum(int value) {
		ghostNum = value;
	}
	
	public int getGhostNum() {
		return ghostNum;
	}
	
}
